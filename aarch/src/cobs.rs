impl<'a, const N: usize> Iterator for &'a mut CobsDecoder<N> {
    type Item = u8;
    fn next(&mut self) -> Option<Self::Item> {
        let b = if let Some(b) = self.queue.read().ok() {
            b
        } else {
            return None;
        };
        self.bytes_since_overhead += 1;

        // The last cobs overhead byte pointed to this byte.
        let at_overhead_marker = 
            self.bytes_since_overhead == self.overhead_byte ||
            self.overhead_byte == 0; // edge case: we just started.

        // cobs encodes away all 0x00 bytes.
        // if we encounter one, we're at a message stop mark
        // iif we're also at an overhead marker position.
        let end_of_message = b == 0;
        if end_of_message {
            // A null byte that an overhead byte didn't point to is illegal.
            // Most likely this means that we missed out on some data.
            // End the message like usual, since it's more likely that
            // this end-of-message byte is valid rather than corrupt.
            // But set an indicator for higher-level code to know what's up.
            self.last_message_error = if !at_overhead_marker {
                Some(CobsError::UnexpectedEndOfMessage)
            } else {
                None
            };
            self.messages_in_queue -= 1;
            self.overhead_byte = 0;
            self.bytes_since_overhead = 0;
            return None;
        }
        // Normally, the overhead byte points to two things.
        // 1) The position where to insert the next 0x00 byte when decoding
        // 2) The (same) position where to find the next overhead byte
        // The last overhead byte pointed to the location where we are now.
        if at_overhead_marker {
            let last_overhead_byte = self.overhead_byte;
            self.overhead_byte = b;
            self.bytes_since_overhead = 0;
            // Edge case:
            // If we're just starting, the very first byte we read is an overhead byte.
            // We know we're in this case because the overhead byte equals 0x00.
            // We allow a one-shot recursion to get started properly.
            if last_overhead_byte == 0x00 {
                return self.next();
            }
            // Normal case: the overhead byte pointed to this location,
            // which means that here should be a null byte.
            if last_overhead_byte != u8::MAX {
                return Some(0);
            }
            // Edge case:
            // When the (previous) overhead byte value was the max value, it means
            // no 0x00 bytes were encountered during encoding for the whole length
            // that the overhead byte can address.
            // So when the overhead byte was the max value, skip inserting a 0x00 byte.
            // This means we'll need to start over to know what to return...
            // This edge case can by definition not happen multiple times in a row,
            // so we tolerate this (one-off) recursion.
            else {
                return self.next();
            }
        }

        // Nothing special going on, no decoding needed.
        Some(b)
    }
}

#[derive(Default,Debug)]
pub struct CobsEncoder<const N: usize> {
    c: CobsCommon<N>,
    last_byte_was_null: bool,

    /// See messages_encoded()
    messages_encoded: usize,
}

impl<const N: usize> Deref for CobsEncoder<N> {
    type Target = CobsCommon<N>;
    fn deref(&self) -> &Self::Target {
        &self.c
    }
}

impl<const N: usize> DerefMut for CobsEncoder<N> {
    fn deref_mut(&mut self) -> &mut Self::Target {
        &mut self.c
    }
}

impl<const N: usize> CobsEncoder<N> {
    /// Total messages ever encoded by this encoder.
    /// Incremented when an end-of-message has been
    /// read from this encoder.
    pub fn messages_encoded(&self) -> usize {
        self.messages_encoded
    }

    pub fn drain(&mut self) -> usize {
        self.overhead_byte = 0;
        self.bytes_since_overhead = 0;
        self.last_byte_was_null = false;
        self.queue.drain()
    }
}

impl<'a, const N: usize> Iterator for &'a mut CobsEncoder<N> {
    type Item = u8;
    fn next(&mut self) -> Option<Self::Item> {

        // Done for now. Reset and end iterator.
        if self.last_byte_was_null {
            self.last_byte_was_null = false;
            self.overhead_byte = 0;
            self.bytes_since_overhead = 0;
            return None;
        }

        // Not at overhead byte position (i.e. null byte or end of message).
        if self.overhead_byte != self.bytes_since_overhead {
            // This read() can not fail, since we know the size of the
            // buffer from computing the overhead byte earlier.
            self.bytes_since_overhead += 1;
            return self.queue.read().ok();

        // The last overhead byte actually pointed to the end of the message.
        } else if self.queue.is_empty() {
            // Send a null byte.
            self.last_byte_was_null = true;
            self.messages_encoded += 1;
            return Some(0);

        // Time to write an overhead byte, and compute the next one.
        } else {
            // Read out the zero byte from the buffer.
            // Unless we just started encoding.
            // Or the last overhead byte was not pointing to a null byte.
            if ![u8::MIN, u8::MAX].contains(&self.overhead_byte) {
                let _ = self.queue.read();
            }
            // Compute and write the next overhead byte.
            self.overhead_byte = match self.queue.next_zero() {
                Some(n) if n >= u8::MAX as usize => {
                    u8::MAX
                },
                Some(n) => n as u8,
                None => {
                    let used = self.queue.used();
                    if used >= u8::MAX as usize {
                        u8::MAX
                    } else {
                        used as u8 + 1
                    }
                }
            };
            self.bytes_since_overhead = 1; // already incremented for next time.
            return Some(self.overhead_byte);
        }
    }
}

