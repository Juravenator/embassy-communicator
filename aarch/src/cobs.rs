#[derive(Default,Debug)]
pub struct CobsDecoder<const N: usize> {
    c: CobsCommon<N>,

    /// See last_message_error()
    last_message_error: Option<CobsError>,

    /// See messages_in_queue()
    messages_in_queue: usize,

    /// See messages_seen()
    messages_seen: usize,
}

impl<const N: usize> CobsDecoder<N> {

    /// Amount of messages waiting in buffer,
    /// based on amount of null bytes present.
    /// Incremented when a null byte is written.
    /// Decremented when a null byte is read
    /// (i.e when a message is fully read from the buffer).
    pub fn messages_in_queue(&self) -> usize {
        self.messages_in_queue
    }

    /// If the decoder saw illegal stuff in the last message.
    /// This value will be set/unset after a message has been fully READ
    /// from the decoder, NOT when written to.
    /// This because a buffer can contain multiple messages. If we'd
    /// set this value at write time, you'd have no way of knowing _which_
    /// message is in error.
    pub fn last_message_error(&self) -> Option<CobsError> {
        self.last_message_error
    }

    /// Total messages ever decoded by this decoder.
    /// Incremented when a null byte is written.
    pub fn messages_seen(&self) -> usize {
        self.messages_seen
    }

    pub fn drain(&mut self) -> usize {
        self.overhead_byte = 0;
        self.bytes_since_overhead = 0;
        self.messages_in_queue = 0;
        self.last_message_error = None;
        self.queue.drain()
    }
}

impl<const N: usize> Deref for CobsDecoder<N> {
    type Target = CobsCommon<N>;
    fn deref(&self) -> &Self::Target {
        &self.c
    }
}

impl<const N: usize> DerefMut for CobsDecoder<N> {
    fn deref_mut(&mut self) -> &mut Self::Target {
        &mut self.c
    }
}

