#[derive(Debug, Copy, Clone, Eq, PartialEq, Format)]
pub enum CobsError {
    UnexpectedEndOfMessage,
}

#[derive(Default,Debug)]
pub struct CobsCommon<const N: usize> {
    queue: Queue<N>,

    /// Value of last seen overhead byte.
    overhead_byte: u8,
    /// Amount of bytes read since last overhead byte.
    bytes_since_overhead: u8,
}

impl<const N: usize> Writer for CobsCommon<N> {
    fn write(&mut self, byte: u8) -> Result<(), WriterError> {
        self.queue.write(byte)
    }
}
