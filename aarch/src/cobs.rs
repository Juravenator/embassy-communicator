impl<const N: usize> Writer for CobsDecoder<N> {
    fn write(&mut self, byte: u8) -> Result<(), WriterError> {
        if byte == 0x00 {
            self.messages_in_queue += 1;
            self.messages_seen += 1;
        }
        self.c.write(byte)
    }
}

